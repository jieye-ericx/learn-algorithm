/**
 * Definition for singly-linked list.
 * function ListNode(val, next) {
 *     this.val = (val===undefined ? 0 : val)
 *     this.next = (next===undefined ? null : next)
 * }
 */
/**
 * @param {ListNode} l1
 * @param {ListNode} l2
 * @return {ListNode}
 */
var mergeTwoLists = function (l1, l2) {
  if (!l1) return l2;
  if (!l2) return l1;
  let list1 = l1,
    list2 = l2,
    ans,
    cur;
  if (l1.val > l2.val) {
    list2 = list2.next;
    ans = l2;
  } else {
    list1 = list1.next;
    ans = l1;
  }
  cur = ans;
  while (list1 && list2) {
    if (list1.val > list2.val) {
      cur.next = list2;
      list2 = list2.next;
    } else {
      cur.next = list1;
      list1 = list1.next;
    }
    cur = cur.next;
  }
  if (list1) {
    cur.next = list1;
  } else {
    cur.next = list2;
  }
  return ans;
};
