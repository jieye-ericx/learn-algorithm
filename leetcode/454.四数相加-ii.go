// !此为官方答案，我在20230216面试中想的比这个多用了一个map，这里直接计算ans += countAB[-v-w]更加巧妙
// @lc code=start
func fourSumCount(a, b, c, d []int) (ans int) {
	countAB := map[int]int{}
	for _, v := range a {
		for _, w := range b {
			countAB[v+w]++
		}
	}
	for _, v := range c {
		for _, w := range d {
			ans += countAB[-v-w]
		}
	}
	return
}

